package main

import (
	"fmt"
)

func main() {
	//String
	fmt.Println("Hello World")
	//Integer
	fmt.Println(1)
	//Float
	fmt.Println(1.22)
	//Boolean
	fmt.Println(true)
	fmt.Println(false)

	// Float calculation
	fmt.Println(10.2 / 2.0) // 5.1
	fmt.Println(7.0 / 3.0)  //2.333333
}
